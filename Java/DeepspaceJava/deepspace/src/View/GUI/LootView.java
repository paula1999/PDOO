    void setLoot (LootToUI lUI) {
        lNSupplies.setText(Integer.toString(lUI.getnSupplies()));
        lNWeapons.setText(Integer.toString(lUI.getnWeapons()));
        lNShields.setText(Integer.toString(lUI.getnShields()));
        lNHangars.setText(Integer.toString(lUI.getnHangars()));
        lNMedals.setText(Integer.toString(lUI.getnMedals()));
        lT.setVisible(true);
        
        if (lUI.isSpaceCity())
            lT.setText("Ciudad Espacial");
        else if (lUI.isGetEfficient())
            lT.setText("Estaci√≥n Eficiente");
        else
            lT.setVisible(false);
        
        repaint();
    }

